  static List<String> allConstituencies() {
    return _unique(constituencies);
  }

  static List<String> constituenciesForDistrict(String district) {
    final mapped = _constituenciesByDistrict[district];
    if (mapped == null || mapped.isEmpty) {
      return allConstituencies();
    }
    return _unique(mapped);
  }

  static bool hasSpecificDistrictMapping(String district) {
    final mapped = _constituenciesByDistrict[district];
    return mapped != null && mapped.isNotEmpty;
  }

  static List<String> _unique(List<String> values) {
    return {...values}.toList(growable: false);
  }
